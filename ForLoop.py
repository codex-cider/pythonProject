# Loops
print("***************  Loops ****************\n")

print("*************** For Loop ****************\n")
FruitsList = ["Apple", "Banana", "Grapes", "Nuts", "Mango"]
for fruit in FruitsList:
    print(fruit)


print("*************** While Loop ****************\n")
var1 = 1
while var1 <= 10:
    print("Loop Working", var1)
    var1 = var1 + 1

